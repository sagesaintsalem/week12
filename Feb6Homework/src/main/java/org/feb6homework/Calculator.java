package org.feb6homework;

public class Calculator {
    public Calculator() {
    }
        public int add(int i, int j){
            return i + j;
        }

        public int subtract(int i, int j) {
            return i - j;
        }

        public int multiply(int i, int j) {
            return i * j;
        }

        public double divide(double i, double j) {
            return i / j;
        }
}
